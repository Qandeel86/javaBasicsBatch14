package com.syntax.class07;

public class DoWhileDemo {

	public static void main(String[] args) {
	
		//while loop has a condition that must be checked first before
		//entering block of code.
		//Do while first lets you print something without a condition first
		
		
		
		int num=1;
		
		while(num<=3) {
			System.out.println("I am while loop");
			num++;
		}

		
		System.out.println("______________________");
		
		int num1=1;
		
		do {
			System.out.println("I am do while loop");
			num1++;
		}while(num1<=3);
		
	
	
	
	
	
	}

}
